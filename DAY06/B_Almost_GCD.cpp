int gcd(int a, int b) {
    while (b) {
        int temp = b;
        b = a % b;
        a = temp;
    }
    return a;
}

int main() {
    int N;
    cin >> N;

    vector<int> A(N);
    for (int i = 0; i < N; ++i) {
        cin >> A[i];
    }

    int maxGCD = 0;
    int maxGCDNumber = 0;

    for (int k = 2; k <= 1000; ++k) {
        int gcdCount = 0;
        for (int i = 0; i < N; ++i) {
            if (A[i] % k == 0) {
                gcdCount++;
            }
        }

        if (gcdCount >= maxGCD) {
            maxGCD = gcdCount;
            maxGCDNumber = k;
        }
    }

    cout << maxGCDNumber << endl;

    return 0;
}
